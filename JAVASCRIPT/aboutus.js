function myFunction() {
  var point = document.getElementById("point");
  var addText = document.getElementById("add");
  var readmoreText = document.getElementById("readmore");

  if (point.style.display === "none") {
    point.style.display = "inline";
   readmoreText.innerHTML = "Read more";
    addText.style.display = "none";
  } else {
    point.style.display = "none";
    readmoreText.innerHTML = "Read less";
    addText.style.display = "inline";
  }
}

